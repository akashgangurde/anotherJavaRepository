
public class ArithmeticOperators {

	public static void main(String[] args) {
		 arithmeticOperators(10, 5);

	}
	
	 public static void arithmeticOperators(int a, int b) {
		 System.out.println(a + b);
		 System.out.println(a - b);
		 System.out.println(a * b);
		 System.out.println(a / b);
		 System.out.println(a % b);
		 }


}
